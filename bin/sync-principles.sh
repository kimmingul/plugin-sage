#!/usr/bin/env bash
# Extract principles and marketplace-publishing content from the source doc
# into generated Skill files. Idempotent: deletes outputs and regenerates.
#
# Usage: bin/sync-principles.sh

set -euo pipefail

PLUGIN_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
SOURCE_DOC="${PLUGIN_ROOT}/../claude-plugin-harness-best-practices-en.md"
if [[ -n "${SOURCE_DATE_EPOCH:-}" ]]; then
  LAST_SYNC_DATE="$(date -u -r "$SOURCE_DATE_EPOCH" +%Y-%m-%d 2>/dev/null || date -u -d "@$SOURCE_DATE_EPOCH" +%Y-%m-%d)"
else
  LAST_SYNC_DATE="$(date -u +%Y-%m-%d)"
fi

HARNESS_SKILL_DIR="${PLUGIN_ROOT}/skills/harness-principles"
MARKET_SKILL_DIR="${PLUGIN_ROOT}/skills/marketplace-publishing"

log()  { printf '[sync] %s\n' "$*"; }
die()  { printf '[sync] ERROR: %s\n' "$*" >&2; exit 1; }

if [[ ! -f "$SOURCE_DOC" ]]; then
  log "Source doc not found at $SOURCE_DOC — skipping sync."
  log "(This is expected when the plugin is installed standalone.)"
  exit 0
fi

log "Source: $SOURCE_DOC"
log "Sync date: $LAST_SYNC_DATE"

# ---------------------------------------------------------------------------
# extract_principles: parse Part 0 of the source doc
# Writes: ${HARNESS_SKILL_DIR}/SKILL.md
#         ${HARNESS_SKILL_DIR}/references/NN-<slug>.md  (10 files)
# ---------------------------------------------------------------------------
extract_principles() {
  local refs_dir="${HARNESS_SKILL_DIR}/references"
  mkdir -p "$refs_dir"
  rm -f "$refs_dir"/*.md "${HARNESS_SKILL_DIR}/SKILL.md"

  # Slice Part 0 into a temp file (from "## Part 0" up to the next "## Part ")
  local part0
  part0="$(mktemp)"
  awk '
    /^## Part 0/ { capture=1 }
    capture && /^## Part [1-9]/ { exit }
    capture { print }
  ' "$SOURCE_DOC" > "$part0"

  if [[ ! -s "$part0" ]]; then
    die "Could not find '## Part 0' in source doc"
  fi

  # Split into per-principle files keyed on "### Principle N: <Name>"
  # Source uses colon separator: "### Principle N: Name"
  awk -v OUT="$refs_dir" '
    function slugify(s,   out) {
      out = tolower(s)
      gsub(/[^a-z0-9]+/, "-", out)
      sub(/^-+/, "", out); sub(/-+$/, "", out)
      return out
    }
    /^### Principle [0-9]+[: ]/ {
      if (current != "") { close(current) }
      num = $3; gsub(/[^0-9]/, "", num)
      name = $0; sub(/^### Principle [0-9]+[: ]+/, "", name)
      slug = slugify(name)
      current = sprintf("%s/%02d-%s.md", OUT, num, slug)
      printf "# Principle %d: %s\n\n", num, name > current
      # Emit one index line: "INDEX N|Name|NN-slug"
      printf "INDEX %d|%s|%02d-%s\n", num, name, num, slug
      next
    }
    current != "" { print > current }
  ' "$part0" > "${part0}.index"

  # Build the PRINCIPLES_INDEX block
  local index_lines
  index_lines=$(awk -F'|' '/^INDEX / { sub(/^INDEX /, ""); printf "%d. **%s** — see `references/%s.md`\n", $1, $2, $3 }' "${part0}.index")

  # Prepend AUTO-GENERATED marker to each reference file
  for f in "$refs_dir"/*.md; do
    local tmp; tmp="$(mktemp)"
    {
      printf '<!-- AUTO-GENERATED by plugin-sage/bin/sync-principles.sh.\n'
      printf '     Source: claude-plugin-harness-best-practices-en.md § Part 0.\n'
      printf '     Last sync: %s.\n' "$LAST_SYNC_DATE"
      printf '     Edits will be overwritten. -->\n'
      cat "$f"
    } > "$tmp"
    mv "$tmp" "$f"
  done

  # Render SKILL.md from template with {{PRINCIPLES_INDEX}} and {{LAST_SYNC_DATE}}
  local tmpl="${PLUGIN_ROOT}/templates/harness-principles.SKILL.md.tmpl"
  local idx_file; idx_file="$(mktemp)"
  printf '%s' "$index_lines" > "$idx_file"
  awk -v idx_file="$idx_file" -v date="$LAST_SYNC_DATE" '
    /\{\{PRINCIPLES_INDEX\}\}/ {
      while ((getline line < idx_file) > 0) print line
      close(idx_file)
      next
    }
    { gsub(/\{\{LAST_SYNC_DATE\}\}/, date); print }
  ' "$tmpl" > "${HARNESS_SKILL_DIR}/SKILL.md"
  rm -f "$idx_file"

  rm -f "$part0" "${part0}.index"
  log "extract_principles: wrote SKILL.md + $(ls "$refs_dir" | wc -l | tr -d ' ') references"
}

extract_principles

# ---------------------------------------------------------------------------
# extract_marketplace: parse §4.9 + Part 4 checklist marketplace block
# Writes: ${MARKET_SKILL_DIR}/SKILL.md
#         ${MARKET_SKILL_DIR}/references/{schema,source-types,strict-mode,
#             error-messages,warnings,gotchas}.md
#         ${MARKET_SKILL_DIR}/checklists/pre-upload.md
# ---------------------------------------------------------------------------
extract_marketplace() {
  local refs_dir="${MARKET_SKILL_DIR}/references"
  local checks_dir="${MARKET_SKILL_DIR}/checklists"
  mkdir -p "$refs_dir" "$checks_dir"
  rm -f "$refs_dir"/*.md "$checks_dir"/*.md "${MARKET_SKILL_DIR}/SKILL.md"

  # Slice §4.9 body into a temp file
  # Exit conditions: next major section (H2), or any H3 other than 4.9 itself.
  # H4 subheadings inside 4.9 are unaffected because they start with "####".
  local sec
  sec="$(mktemp)"
  awk '
    /^### 4\.9 / { capture=1 }
    capture && /^## / { exit }
    capture && /^### / && $0 !~ /^### 4\.9 / { exit }
    capture { print }
  ' "$SOURCE_DOC" > "$sec"

  [[ -s "$sec" ]] || die "Could not find '### 4.9' in source doc"

  write_ref_file() {
    # $1 = filename (without dir)
    # stdin = body
    local out="$refs_dir/$1"
    {
      printf '<!-- AUTO-GENERATED by plugin-sage/bin/sync-principles.sh.\n'
      printf '     Source: claude-plugin-harness-best-practices-en.md § 4.9.\n'
      printf '     Last sync: %s. -->\n' "$LAST_SYNC_DATE"
      cat
    } > "$out"
  }

  # --- schema.md: the Location + Required Structure subsection ---
  awk '
    /^#### Location and Required Structure/ { capture=1 }
    capture && /^#### The 5 Plugin Source Types/ { exit }
    capture { print }
  ' "$sec" | write_ref_file "schema.md"

  # --- source-types.md ---
  awk '
    /^#### The 5 Plugin Source Types/ { capture=1 }
    capture && /^#### strict Mode/ { exit }
    capture { print }
  ' "$sec" | write_ref_file "source-types.md"

  # --- strict-mode.md ---
  awk '
    /^#### strict Mode/ { capture=1 }
    capture && /^#### Validation Commands/ { exit }
    capture { print }
  ' "$sec" | write_ref_file "strict-mode.md"

  # --- error-messages.md ---
  awk '
    /^#### Error Message Reference/ { capture=1 }
    capture && /^#### Warnings/ { exit }
    capture { print }
  ' "$sec" | write_ref_file "error-messages.md"

  # --- warnings.md ---
  awk '
    /^#### Warnings/ { capture=1 }
    capture && /^#### Gotchas/ { exit }
    capture { print }
  ' "$sec" | write_ref_file "warnings.md"

  # --- gotchas.md (tail of §4.9) ---
  awk '
    /^#### Gotchas/ { capture=1 }
    capture { print }
  ' "$sec" | write_ref_file "gotchas.md"

  # --- checklists/pre-upload.md from Part 4 checklist "Marketplace upload" block ---
  local checklist
  checklist="$(awk '
    /^\*\*Marketplace upload\*\*/ { capture=1; next }
    capture && /^\*\*/ { exit }
    capture && /^$/ && seenitems { exit }
    capture && /^- \[ \]/ { seenitems=1; print; next }
    capture { next }
  ' "$SOURCE_DOC")"

  {
    printf '<!-- AUTO-GENERATED by plugin-sage/bin/sync-principles.sh.\n'
    printf '     Source: claude-plugin-harness-best-practices-en.md § Part 4 Checklist.\n'
    printf '     Last sync: %s. -->\n\n' "$LAST_SYNC_DATE"
    printf '# Pre-Upload Checklist\n\n'
    printf 'Copy and check off each item before uploading.\n\n'
    printf '%s\n' "$checklist"
  } > "$checks_dir/pre-upload.md"

  # --- SKILL.md ---
  local tmpl="${PLUGIN_ROOT}/templates/marketplace-publishing.SKILL.md.tmpl"
  awk -v date="$LAST_SYNC_DATE" '
    { gsub(/\{\{LAST_SYNC_DATE\}\}/, date); print }
  ' "$tmpl" > "${MARKET_SKILL_DIR}/SKILL.md"

  rm -f "$sec"
  log "extract_marketplace: wrote SKILL.md + $(ls "$refs_dir" | wc -l | tr -d ' ') references + pre-upload checklist"
}

extract_marketplace

log "Sync complete."
